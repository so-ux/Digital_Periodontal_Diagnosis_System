        x_reg = self.dp1(x_reg)
        x_reg = self.fc3(x_reg)
        x_reg = x_reg.view(batch_size, num_points, -1)

        x = x_coord.transpose(2, 1) + x_reg
        return x, x_conf[:, 0, :]
